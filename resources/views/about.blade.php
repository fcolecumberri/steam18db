    <div class="block my-6 has-text-justified">
        <h1 class="title my-3">About</h1>
        <p>
        Several years ago Steam decided to allow 18+ content on their platform.
        However depending on the type of content,
        theme of the game,
        media controversy,
        and the roll of cosmic dices,
        Steam still bans games at will.
        This led to several 18+ game developers to create censored Steam-friendly version of their games
        and publish 18+ patches somewhere else.
        Because of this, now it is not trivial to distinguish a
        15+/17+ game from an 18+ game disguised as a 15+/17+ game
        forced to search for a patch that might or might not exist.
        </p>
        <p class="mt-4">
        There are some big 18+ game developers/publishers like kagura and shiravune that are well known and it's obvious a patch exists and where to find it.
        However there are other game developers/publishers, like JAST, whose repertoire is a mix of 18+, 17+, and 15+ making it less obvious.
        </p>
        <p class="mt-4">
            This page aims to be the solution to this problem by allowing users to trivially find game patches.
        </p>
    </div>
    <div class="block my-6 has-text-justified">
